package model;

public class CountModel {
	private int countTotal;
	private int countHistory;
	private int countGeo;

	public int getCountTotal() {
		return countTotal;
	}

	public void setCountTotal(int countTotal) {
		this.countTotal = countTotal;
	}

	public int getCountHistory() {
		return countHistory;
	}

	public void setCountHistory(int countHistory) {
		this.countHistory = countHistory;
	}

	public int getCountGeo() {
		return countGeo;
	}

	public void setCountGeo(int countGeo) {
		this.countGeo = countGeo;
	}

}
